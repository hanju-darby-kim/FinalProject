package com.test.spring.dto;

public class ColleagueEvaluationDTO {
	private String seq;
	private String getSeq;
	private String doSeq;
	private String distinction;
	private String amount;
	private String attitude;
	private String contribution;
	private String descriptiveEvaluation;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getGetSeq() {
		return getSeq;
	}
	public void setGetSeq(String getSeq) {
		this.getSeq = getSeq;
	}
	public String getDoSeq() {
		return doSeq;
	}
	public void setDoSeq(String doSeq) {
		this.doSeq = doSeq;
	}
	public String getDistinction() {
		return distinction;
	}
	public void setDistinction(String distinction) {
		this.distinction = distinction;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getAttitude() {
		return attitude;
	}
	public void setAttitude(String attitude) {
		this.attitude = attitude;
	}
	public String getContribution() {
		return contribution;
	}
	public void setContribution(String contribution) {
		this.contribution = contribution;
	}
	public String getDescriptiveEvaluation() {
		return descriptiveEvaluation;
	}
	public void setDescriptiveEvaluation(String descriptiveEvaluation) {
		this.descriptiveEvaluation = descriptiveEvaluation;
	}
	
}
