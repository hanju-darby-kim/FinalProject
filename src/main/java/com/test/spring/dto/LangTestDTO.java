package com.test.spring.dto;

public class LangTestDTO {

	private String seq;
	private String studentSeq;
	private String languageSeq;
	private String langTestSeq;
	private String score;
	private String testDate;
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getStudentSeq() {
		return studentSeq;
	}
	public void setStudentSeq(String studentSeq) {
		this.studentSeq = studentSeq;
	}
	public String getLanguageSeq() {
		return languageSeq;
	}
	public void setLanguageSeq(String languageSeq) {
		this.languageSeq = languageSeq;
	}
	public String getLangTestSeq() {
		return langTestSeq;
	}
	public void setLangTestSeq(String langTestSeq) {
		this.langTestSeq = langTestSeq;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getTestDate() {
		return testDate;
	}
	public void setTestDate(String testDate) {
		this.testDate = testDate;
	}
	
	
}
